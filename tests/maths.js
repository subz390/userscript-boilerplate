/**
 * Adds two numbers together
 * @param {number} a first number
 * @param {number} b second number
 * @return {number} the sum of the two numbers
 */
export function sum(a, b) {
  return a + b
}

/**
 * Subtracts two numbers from one another
 * @param {number} a first number
 * @param {number} b second number
 * @return {number} the result
 */
export function subtract(a, b) {
  return a - b
}

/**
 * Divides one number into the other
 * @param {number} a first number
 * @param {number} b second number
 * @return {number} the result
 */
export function divide(a, b) {
  return a / b
}

/**
 * Multiplies two numbers together
 * @param {number} a first number
 * @param {number} b second number
 * @return {number} the result
 */
export function multiply(a, b) {
  return a * b
}

/**
 * Calculates the cubed value of a number
 * @param {number} x the number
 * @return {number} the result
 */
export function cubed(x) {
  return x * x * x
}

/**
 * Returns true if a is greater than b, else false
 * @param {number} a first number
 * @param {number} b second number
 * @return {boolean} result
 */
export function isGreaterThan(a, b) {
  return a > b ? true : false
}
